# Default FE buckets for analysis / 默认 FE 分桶用于分析
DEFAULT_FE_BUCKETS = [
    (0, 1e5), (1e5, 2e5), (2e5, 3e5), (3e5, 4e5), (4e5, 5e5),
    (5e5, 6e5), (6e5, 7e5), (7e5, 8e5), (8e5, 9e5), (9e5, 1e6)
]

# Index of s12 (remaining FEs ratio) in state / s12（剩余FEs比例）在状态中的索引
S12_INDEX = 11

# Default record interval / 默认记录间隔
DEFAULT_RECORD_EVERY = 1


