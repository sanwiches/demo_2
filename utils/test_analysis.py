def fe_bucket_id(fe_used: float, buckets: List[Tuple[float, float]] = DEFAULT_FE_BUCKETS) -> int:
    """
    Map function evaluation usage to bucket index.
    将函数评估使用量映射到分桶索引。

    Args:
        fe_used: Number of function evaluations used / 使用的函数评估次数
        buckets: List of (lower, upper) bucket boundaries / 分桶边界列表

    Returns:
        Bucket index / 分桶索引

    Example:
        >>> fe_bucket_id(50000)  # Returns 0
        >>> fe_bucket_id(150000) # Returns 1
    """
    for i, (lo, hi) in enumerate(buckets):
        if lo <= fe_used < hi:
            return i
    return len(buckets)


@torch.no_grad()
def extract_featurese_weights(
    actor: torch.nn.Module,
    state: torch.Tensor
) -> Dict[str, torch.Tensor]:
    """
    Extract FeatureSE weights from actor network.
    从 Actor 网络中提取 FeatureSE 权重。

    This function extracts the attention weights generated by the FeatureSE
    module, including final weights, feature weights, group weights, and
    group statistics.

    此函数提取 FeatureSE 模块生成的注意力权重，包括最终权重、特征权重、
    组权重和组统计量。

    Args:
        actor: Actor network with FeatureSE module / 带 FeatureSE 模块的 Actor 网络
        state: Current state tensor [B, F] / 当前状态张量

    Returns:
        Dictionary containing:
            - w: Final weights [B, F] / 最终权重
            - g_full: Group weights expanded to features [B, F] / 扩展到特征的组权重
            - f: Feature weights [B, F] / 特征权重
            - g: Group weights [B, G] / 组权重
            - s: Group statistics [B, G] / 组统计量

    Example:
        >>> weights = extract_featurese_weights(agent.actor, state)
        >>> print(weights['w'].shape)  # [B, 16]
    """
    fs = actor.featurese

    # Use compute_weights if available (preferred method) / 如果可用则使用 compute_weights（首选方法）
    if hasattr(fs, "compute_weights"):
        return fs.compute_weights(state)

    # Inline computation (fallback, sync with FeatureSE.forward) / 内联计算（后备方法，与 FeatureSE.forward 同步）
    x = state if state.dtype == torch.float32 else state.float()
    x_ln = fs.ln(x)  # Layer normalization / 层归一化

    # Compute group statistics / 计算组统计量
    stats = []
    for (l, r) in fs.groups:
        stats.append(x_ln[:, l:r].mean(dim=1, keepdim=True))
    s = torch.cat(stats, dim=1)                             # [B, G]
    g = torch.sigmoid(fs.g_fc2(fs.act(fs.g_fc1(s))))        # [B, G]

    # Expand group weights to feature level / 将组权重扩展到特征级别
    g_full_parts = []
    for i, (l, r) in enumerate(fs.groups):
        g_full_parts.append(g[:, i:i+1].repeat(1, r-l))
    g_full = torch.cat(g_full_parts, dim=1)                 # [B, F]

    # Compute feature weights / 计算特征权重
    f = torch.sigmoid(fs.f_fc2(fs.act(fs.f_fc1(x_ln))))     # [B, F]
    w = (1.0 - fs.gate_lambda) + fs.gate_lambda * (g_full * f)

    return {"w": w, "g_full": g_full, "f": f, "g": g, "s": s}


