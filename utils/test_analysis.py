class TestAnalyzer:
    """
    Comprehensive test analyzer combining multiple recording functions.
    综合测试分析器，组合多种记录功能。

    This analyzer provides a unified interface for recording:
    - Basic test results (fitness, actions)
    - FeatureSE weights
    - Time tracking

    该分析器提供统一接口用于记录：
    - 基本测试结果（适应度、动作）
    - FeatureSE 权重
    - 时间跟踪

    Example:
        >>> analyzer = TestAnalyzer(opts)
        >>> analyzer.start_test(fun_id, n_envs)
        >>>
        >>> while not done:
        >>>     # ... agent interaction ...
        >>>     analyzer.record_step(actor, state, step, rewards, is_end, info)
        >>>
        >>> result = analyzer.end_test()
        >>> analyzer.save_results(output_dir)
    """

    def __init__(
        self,
        opts,
        enable_featurese: bool = True,
        buckets: List[Tuple[float, float]] = DEFAULT_FE_BUCKETS,
        record_every: int = DEFAULT_RECORD_EVERY
    ):
        """
        Initialize test analyzer / 初始化测试分析器

        Args:
            opts: Configuration options / 配置选项
            enable_featurese: Whether to record FeatureSE weights / 是否记录 FeatureSE 权重
            buckets: FE bucket boundaries / FE 分桶边界
            record_every: Recording interval / 记录间隔
        """
        self.opts = opts
        self.enable_featurese = enable_featurese
        self.test_recorder: Optional[TestRecorder] = None
        self.featurese_recorder: Optional[FeatureSERecorder] = None

        if enable_featurese:
            self.featurese_recorder = FeatureSERecorder(opts, buckets, record_every)

    def start_test(self, fun_id: int, n_envs: int) -> None:
        """
        Start a new test session.
        开始新的测试会话。

        Args:
            fun_id: Problem function ID / 问题函数 ID
            n_envs: Number of environments / 环境数量
        """
        self.test_recorder = TestRecorder(n_envs, fun_id)
        self.test_recorder.start_timer()
        if self.featurese_recorder:
            self.featurese_recorder.clear()

    def record_step(
        self,
        actor: torch.nn.Module,
        state: torch.Tensor,
        step: int,
        actions: torch.Tensor,
        rewards: List,
        is_end: List[bool],
        info: List[Dict]
    ) -> None:
        """
        Record information for one step.
        记录一步的信息。

        Args:
            actor: Actor network / Actor 网络
            state: Current state / 当前状态
            step: Current step / 当前步数
            actions: Actions taken / 采取的动作
            rewards: Rewards received / 收到的奖励
            is_end: Episode termination flags / 回合终止标志
            info: Environment info / 环境信息
        """
        if self.test_recorder is None:
            raise RuntimeError("Must call start_test() before record_step()")

        fun_id = self.test_recorder.fun_id
        env_ids = list(range(self.test_recorder.n_envs))

        # Record FeatureSE weights / 记录 FeatureSE 权重
        if self.featurese_recorder:
            self.featurese_recorder.record(actor, state, step, fun_id, env_ids)

        # Record fitness and actions / 记录适应度和动作
        actions_cpu = actions.detach().cpu()
        for i in range(len(env_ids)):
            if not is_end[i]:
                if 'fitness_record' in info[i]:
                    self.test_recorder.record_fitness(i, info[i]['fitness_record'].copy())
            self.test_recorder.record_action(i, tuple(actions_cpu[i].tolist()))

    def end_test(self) -> TestResult:
        """
        End test session and return results.
        结束测试会话并返回结果。

        Returns:
            TestResult object / TestResult 对象
        """
        if self.test_recorder is None:
            raise RuntimeError("Must call start_test() before end_test()")
        self.test_recorder.stop_timer()
        return self.test_recorder.get_result()

    def save_results(self, output_dir: Path) -> None:
        """
        Save all recorded results to directory.
        将所有记录的结果保存到目录。

        Args:
            output_dir: Directory to save results / 保存结果的目录
        """
        output_dir = Path(output_dir)
        output_dir.mkdir(parents=True, exist_ok=True)

        # Save basic results / 保存基本结果
        if self.test_recorder:
            result = self.test_recorder.get_result()
            data = {"WCC": result.fitness_records, 'WCC_time': [result.time_elapsed]}
            actions_df = pd.DataFrame(result.actions_record)
            actions_df.to_csv(output_dir / 'actions.csv', index=False, header=False)

        # Save FeatureSE results / 保存 FeatureSE 结果
        if self.featurese_recorder:
            self.featurese_recorder.save(output_dir)
