@dataclass
class TestResult:
    """
    Container for test results.
    测试结果容器。

    Attributes:
        fitness_records: Fitness history for each environment / 每个环境的适应度历史
        actions: Action history for each environment / 每个环境的动作历史
        time_elapsed: Time taken for the test / 测试耗时
        fun_id: Problem function ID / 问题函数 ID
    """
    fitness_records: List[List[float]] = field(default_factory=list)
    actions: List[List[Tuple[int, int]]] = field(default_factory=list)
    time_elapsed: float = 0.0
    fun_id: int = -1

    def to_dict(self) -> Dict[str, Any]:
        """Convert to dictionary / 转换为字典"""
        return {
            "fitness_records": self.fitness_records,
            "actions": self.actions,
            "time_elapsed": self.time_elapsed,
            "fun_id": self.fun_id
        }


@dataclass
class FeatureSERecord:
    """
    Container for FeatureSE weight records.
    FeatureSE 权重记录容器。

    Attributes:
        feature_rows: Per-feature weight records / 每特征权重记录
        group_rows: Per-group weight records / 每组权重记录
    """
    feature_rows: List[Dict[str, Any]] = field(default_factory=list)
    group_rows: List[Dict[str, Any]] = field(default_factory=list)

    def to_dataframes(self) -> Tuple[pd.DataFrame, pd.DataFrame]:
        """
        Convert records to pandas DataFrames.
        将记录转换为 pandas DataFrame。

        Returns:
            Tuple of (feature_df, group_df) / (特征 DataFrame, 组 DataFrame)
        """
        return pd.DataFrame(self.feature_rows), pd.DataFrame(self.group_rows)

    def save(self, output_dir: Path) -> None:
        """
        Save records to CSV files.
        将记录保存到 CSV 文件。

        Args:
            output_dir: Directory to save files / 保存文件的目录
        """
        output_dir = Path(output_dir)
        output_dir.mkdir(parents=True, exist_ok=True)

        feat_df, group_df = self.to_dataframes()

        # Save detailed records / 保存详细记录
        feat_df.to_csv(output_dir / 'featurese_weights.csv', index=False)
        group_df.to_csv(output_dir / 'featurese_groups.csv', index=False)

        # Save aggregated records / 保存聚合记录
        agg_feat = feat_df.groupby(["bucket", "feature"])["w"].mean().reset_index()
        agg_group = group_df.groupby(["bucket", "group"])["g"].mean().reset_index()
        agg_feat.to_csv(output_dir / 'featurese_weights_agg.csv', index=False)
        agg_group.to_csv(output_dir / 'featurese_groups_agg.csv', index=False)


