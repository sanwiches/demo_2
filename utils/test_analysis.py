class FeatureSERecorder:
    """
    Records FeatureSE attention weights during testing.
    在测试期间记录 FeatureSE 注意力权重。

    This recorder collects FeatureSE weights at specified intervals during
    agent testing, organizing them by FE buckets for analysis.

    此记录器在智能体测试期间按指定间隔收集 FeatureSE 权重，
    并按 FE 分桶组织以便分析。

    Attributes:
        opts: Configuration options / 配置选项
        buckets: FE bucket boundaries / FE 分桶边界
        record_every: Recording interval / 记录间隔

    Example:
        >>> recorder = FeatureSERecorder(opts)
        >>> recorder.record(actor, state, step, fun_id, env_ids)
        >>> recorder.save(output_dir)
    """

    def __init__(
        self,
        opts,
        buckets: List[Tuple[float, float]] = DEFAULT_FE_BUCKETS,
        record_every: int = DEFAULT_RECORD_EVERY
    ):
        """
        Initialize FeatureSE recorder / 初始化 FeatureSE 记录器

        Args:
            opts: Configuration options / 配置选项
            buckets: FE bucket boundaries / FE 分桶边界
            record_every: Record every N steps / 每 N 步记录一次
        """
        self.opts = opts
        self.buckets = buckets
        self.record_every = record_every
        self.record = FeatureSERecord()

    def should_record(self, step: int) -> bool:
        """Check if should record at this step / 检查此步是否应记录"""
        return step % self.record_every == 0

    def record(
        self,
        actor: torch.nn.Module,
        state: torch.Tensor,
        step: int,
        fun_id: int,
        env_ids: List[int]
    ) -> None:
        """
        Record FeatureSE weights for current state.
        记录当前状态的 FeatureSE 权重。

        Args:
            actor: Actor network / Actor 网络
            state: Current state [B, F] / 当前状态
            step: Current step / 当前步数
            fun_id: Problem function ID / 问题函数 ID
            env_ids: Environment indices / 环境索引
        """
        if not self.should_record(step):
            return

        # Extract weights / 提取权重
        ws = extract_featurese_weights(actor, state)
        w = ws["w"].detach().cpu().numpy()
        f = ws["f"].detach().cpu().numpy()
        g_full = ws["g_full"].detach().cpu().numpy()
        g = ws["g"].detach().cpu().numpy()
        s = ws["s"].detach().cpu().numpy()

        # Infer FEs used from state / 从状态推断使用的 FEs
        s12 = state[:, S12_INDEX].detach().cpu().numpy()
        fe_used = self.opts.max_fes * (1.0 - s12)
        buckets = np.array([fe_bucket_id(u, self.buckets) for u in fe_used], dtype=np.int64)

        B, F = w.shape
        G = g.shape[1]

        # Record feature-level weights / 记录特征级权重
        for b_idx, env_id in enumerate(env_ids):
            for j in range(F):
                self.record.feature_rows.append({
                    "fun_id": int(fun_id),
                    "step": int(step),
                    "env": int(env_id),
                    "bucket": int(buckets[b_idx]),
                    "fe_used": float(fe_used[b_idx]),
                    "feature": int(j),
                    "w": float(w[b_idx, j]),
                    "f": float(f[b_idx, j]),
                    "g_full": float(g_full[b_idx, j]),
                })

        # Record group-level weights / 记录组级权重
        for b_idx, env_id in enumerate(env_ids):
            for k in range(G):
                self.record.group_rows.append({
                    "fun_id": int(fun_id),
                    "step": int(step),
                    "env": int(env_id),
                    "bucket": int(buckets[b_idx]),
                    "fe_used": float(fe_used[b_idx]),
                    "group": int(k),
                    "g": float(g[b_idx, k]),
                    "s_mean": float(s[b_idx, k])
                })

    def save(self, output_dir: Path) -> None:
        """Save records to directory / 将记录保存到目录"""
        self.record.save(output_dir)

    def clear(self) -> None:
        """Clear all records / 清除所有记录"""
        self.record = FeatureSERecord()


class TestRecorder:
    """
    Records basic test results (fitness and actions).
    记录基本测试结果（适应度和动作）。

    Attributes:
        n_envs: Number of parallel environments / 并行环境数量
        fun_id: Problem function ID / 问题函数 ID
    """

    def __init__(self, n_envs: int, fun_id: int = -1):
        """
        Initialize test recorder / 初始化测试记录器

        Args:
            n_envs: Number of parallel environments / 并行环境数量
            fun_id: Problem function ID / 问题函数 ID
        """
        self.n_envs = n_envs
        self.fun_id = fun_id
        self.fitness_records = [[] for _ in range(n_envs)]
        self.actions_record = [[] for _ in range(n_envs)]
        self.time_start = 0.0
        self.time_end = 0.0

    def start_timer(self) -> None:
        """Start timing / 开始计时"""
        import time
        self.time_start = time.time()

    def stop_timer(self) -> None:
        """Stop timing / 停止计时"""
        import time
        self.time_end = time.time()

    def record_fitness(self, env_id: int, fitness_list: List[float]) -> None:
        """Record fitness for an environment / 记录环境的适应度"""
        self.fitness_records[env_id].extend(fitness_list)

    def record_action(self, env_id: int, action: Tuple[int, int]) -> None:
        """Record action for an environment / 记录环境的动作"""
        self.actions_record[env_id].append(action)

    def get_result(self) -> TestResult:
        """Get test result / 获取测试结果"""
        return TestResult(
            fitness_records=self.fitness_records,
            actions=self.actions_record,
            time_elapsed=self.time_end - self.time_start,
            fun_id=self.fun_id
        )


